package com.mycompany.a2;


//Object Color Class
public class ObjectColor {

	private int r;
	private int g;
	private int b;
	
	public ObjectColor(int r, int g, int b) {
		this.r = r;
		this.g = g;
		this.b = b;
	}
	
	public int getR() {
		return this.r;
	}
	
	public int getG() {
		return this.g;
	}
	
	public int getB(){
		return this.b;
	}

}
